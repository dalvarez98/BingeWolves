public class PetSpeciesDisplay {
	private String petName;
	private String petSpeciesId;
	private String petDisplayId;
	
	/**
	 * @return the petName
	 */
	public String getPetName() {
		return petName;
	}
	/**
	 * @param petName the petName to set
	 */
	public void setPetName(String petName) {
		this.petName = petName;
	}
	public String getPetSpeciesId() {
		return petSpeciesId;
	}
	public void setPetSpeciesId(String petSpeciesId) {
		this.petSpeciesId = petSpeciesId;
	}
	public String getPetDisplayId() {
		return petDisplayId;
	}
	public void setPetDisplayId(String petDisplayId) {
		this.petDisplayId = petDisplayId;
	}
}
